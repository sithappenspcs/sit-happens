import { PassportStrategy } from '@nestjs/passport';
import { Strategy, VerifyCallback } from 'passport-google-oauth20';
import { Injectable } from '@nestjs/common';
import { AuthService } from './auth.service';

@Injectable()
export class GoogleStrategy extends PassportStrategy(Strategy, 'google') {
  constructor(private authService: AuthService) {
    super({
      clientID: process.env.GOOGLE_CLIENT_ID || 'client-id-placeholder',
      clientSecret: process.env.GOOGLE_CLIENT_SECRET || 'client-secret-placeholder',
      callbackURL: process.env.GOOGLE_CALENDAR_REDIRECT_URI || 'http://localhost:4000/auth/google/calendar/callback',
      scope: ['email', 'profile'],
    });
  }

  async validate(accessToken: string, refreshToken: string, profile: any, done: VerifyCallback): Promise<any> {
    const { name, emails, photos } = profile;
    const user = {
      email: emails[0].value,
      firstName: name.givenName,
      lastName: name.familyName,
      picture: photos[0].value,
      accessToken,
      refreshToken,
    };
    
    const dbUser = await this.authService.validateOAuthUser(user);
    done(null, dbUser);
  }
}
